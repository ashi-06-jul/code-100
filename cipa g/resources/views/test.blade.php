                <p class="pad_bot1" style="">Adolescence was quite hard, compared to childhood because at a young age, even if incidents happened, I didn’t realise the reason behind It or its’ meaning. It was in the starting of the adolescence when the environment around me started to change and I realised the sense behind all the comments, ‘jokes’ and ‘preferential’ treatment. Being joked about was not, and is not fun/okay for a person who would be insecure about the way the world looked at them. It used to hurt a lot when friends, and even relatives used to pass comments on the way your face looked. I used to question why I’m different and couldn’t be normal every time I visited the dentist or when I was in an operation theatre. But slowly I started to realise that other humans didn’t matter. Only your family and good friends mattered, which I realised, but later as I grew up. Having a strong support from them and brilliant doctors not only changed the way I looked, but the way I think. </p> 
