public class Switch2
{
    public static void main(String args[])
    {
        int n=20;
        switch(n)
        {
        case 10: System.out.println("Sagar");
        case 20: System.out.println("Garima");
        case 30: System.out.println("Senger");
        default: System.out.println("Not in 10");
        }
    }
}