                <p class="pad_bot1" style="">It was a big responsibility and a great honour to represent India through CIPA on this mega podium for both Col. Amik and me. We started the presentation and after a few jitters, the flow was maintained. Col. Amik was as usual so supportive and controlled situations very well. The presentation was appreciated by the audience and the time allocated to us also got extended.</p>

                <p class="pad_bot1" style="">After the presentation, there was an interactive Q & A session. We supported each other on question being put to us. There were many queries and to be honest, we were not having perfect answers to a few of them. The overall response of audience was overwhelming and encouraging. The interaction with NGO from Philippines was so humble. </p>

