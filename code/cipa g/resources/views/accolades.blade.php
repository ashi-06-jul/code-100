                <p><h5>Cleft India Parent Association (CIPA) was conferred with CPF
 Leadership Award for the year
2016 and chosen among large number of nominees as an outstanding organization
recognized by the Cleft Palate Foundation. The CPF Leadership Award recognizes an
individual or group that has demonstrated exceptional service and leadership on behalf of
individuals with cleft or craniofacial anomalies and is awarded at the Annual Meeting of the
American Cleft Palate-Craniofacial Association (ACPA). Our Patron Dr. Karoon Agrawal
accepted the Leadership Award and was honoured on 07 April 2016 at the Annual Meeting
of the American Cleft Palate-Craniofacial Association (ACPA) in Atlanta, Georgia, USA. He
gave presentation to the audience from large number of countries and the incredible work
being done by CIPA, was applauded by all the members present. In addition, CIPA was
invited to participate in a session during Cleft 2017 Congress held at Chennai in February
2017. This gave an opportunity to CIPA to interact with international community and over
1000 delegates..<br><br>

<h2><strong>“I</strong>naugural Function CIPA”</h2>
<p><b>
The inaugural function of the Cleft India Parents Association (CIPA) was held at
Sant Parmanand Hospital, Civil lines, Delhi 07 Dec 2014. The chief guest for the
function was Dr. Anil Arya, President of Delhi State, Indian Dental Association. Other
dignitaries present were Sh. Satish Kalra, Dr. Mamtaa Carrol &amp; Dr. SC Sood from
Smile train. Other dignitaries from other leading cleft organizations of India were Dr.
Ashish Sabharwal, Dr. Shekhar Agarwal, Dr. Richie Gupta, Dr. SV Kharbanda &amp; Dr.
Aparna Agrawal.<br>
In addition, the star attraction of the event was the presence of
Sh. Surender Sharma whois a nationally &amp; Internationally acclaimed poet and
usually writes comic sketches of himself and his wife.An overwhelming response was seen during the inauguration as the event was
attended by approximately 100 parents and cleft children from various strata of the
society. Smile train officials Mr Satish Kalra, Dr Ashish Sabarwal and Mamtaa Carrol
were present at the event. The event has been supported by the Indian Orthodontic
Society and Indian Dental Association. There was an emotional sharing of
experience by some of the parents on bringing up their cleft children. Subsequently,
a cultural programme was presented by the children giving out a message that this
anomaly should be seen as strength and not a weakness under any circumstances.
Their voluntary participation and the performance was a motivation and confidence
booster both for the parents and the children present during the inauguration
ceremony.
Since then it has grown in stature and its contributions. It is known to majority of the
parents of cleft children in and around Delhi. CIPA has organised many camps and
public interactions independently as well as in association with NGOs. It has helped
many parents in decision making and extended moral support to many parents.</p>

